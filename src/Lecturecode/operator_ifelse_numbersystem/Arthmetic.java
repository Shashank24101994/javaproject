package Lecturecode.operator_ifelse_numbersystem;

public class Arthmetic
{
    public static void main(String[] args) {
        int a=20;
        int b=8;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b); //gives remainder

        double x =4;
        double y =3;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);
    }
}
