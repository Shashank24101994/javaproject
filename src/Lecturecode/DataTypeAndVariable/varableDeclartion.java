package Lecturecode.DataTypeAndVariable;

public class varableDeclartion
{
    public static void main(String[] args) {
        int myNumber =865; //declaration+intilization
        System.out.println(myNumber);
        int yournumber; //declaration
        yournumber=895; //intialization
        System.out.println(yournumber);
        yournumber=45;
        System.out.println(yournumber);


        float myFloat = 5.0f;
        double myDouble = 6.98766;
        System.out.println(myFloat);
        System.out.println(myDouble);

        boolean isVegetarian = true;
        System.out.println(isVegetarian);

        String wishes = "Good Morning";
        System.out.println(wishes);

        char myCharacter = 'K';

    }

}
